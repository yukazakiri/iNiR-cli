package vesktop

import (
	"fmt"
	"os"
	"path/filepath"

	"github.com/yukazakiri/inir-cli/internal/target"
)

type Applier struct{}

const vesktopThemeFileName = "inir-material-you.theme.css"

func (a *Applier) Apply(ctx *target.Context) error {
	if ctx == nil || ctx.Config == nil {
		return fmt.Errorf("vesktop apply: nil context or config")
	}

	if !ctx.Config.WallpaperTheming.EnableVesktop {
		return nil
	}

	colors, err := readVesktopColors(ctx)
	if err != nil {
		return nil
	}

	configDir := resolveVesktopConfigDir(ctx)
	themesDir := filepath.Join(configDir, "themes")
	if err := os.MkdirAll(themesDir, 0755); err != nil {
		return fmt.Errorf("create vesktop themes dir: %w", err)
	}

	themePath := filepath.Join(themesDir, vesktopThemeFileName)
	if err := os.WriteFile(themePath, []byte(generateVesktopCSS(colors)), 0644); err != nil {
		return fmt.Errorf("write vesktop theme css: %w", err)
	}

	return nil
}

func readVesktopColors(ctx *target.Context) (map[string]string, error) {
	palette, err := ctx.ReadPaletteJSON()
	if err != nil {
		palette, err = ctx.ReadColorsJSON()
		if err != nil {
			return nil, err
		}
	}
	return palette, nil
}

func resolveVesktopConfigDir(ctx *target.Context) string {
	base := ctx.XDGConfigHome()
	candidates := []string{
		filepath.Join(base, "vesktop"),
		filepath.Join(base, "Vesktop"),
	}

	for _, dir := range candidates {
		if stat, err := os.Stat(dir); err == nil && stat.IsDir() {
			return dir
		}
	}

	return candidates[0]
}

func generateVesktopCSS(colors map[string]string) string {
	pick := func(key, fallback string) string {
		if v, ok := colorutil.NormalizeHexLower(colors[key]); ok {
			return v
		}
		if v, ok := colorutil.NormalizeHexLower(fallback); ok {
			return v
		}
		return "#000000"
	}

	background := pick("surface", "#1e1e2e")
	surface := pick("surface_container", "#313244")
	foreground := pick("on_surface", "#cdd6f4")
	muted := pick("on_surface_variant", "#9399b2")
	accent := pick("primary", "#cba6f7")
	errorColor := pick("error", "#f38ba8")

	return fmt.Sprintf(`/**
 * iNiR Material You - Vesktop
 * Auto-generated by inir-cli. Do not edit manually.
 */

:root {
  --inir-bg: %s;
  --inir-surface: %s;
  --inir-fg: %s;
  --inir-muted: %s;
  --inir-accent: %s;
  --inir-error: %s;
}

body,
.theme-dark {
  --background-primary: var(--inir-bg) !important;
  --background-secondary: var(--inir-surface) !important;
  --background-tertiary: var(--inir-surface) !important;
  --text-normal: var(--inir-fg) !important;
  --text-muted: var(--inir-muted) !important;
  --brand-500: var(--inir-accent) !important;
  --status-danger: var(--inir-error) !important;
}
`, background, surface, foreground, muted, accent, errorColor)
}
