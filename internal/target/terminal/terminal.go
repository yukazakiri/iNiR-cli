func ensureLineInFile(path, line string) error {
	if err := os.MkdirAll(filepath.Dir(path), 0755); err != nil {
		return err
	}

	content := ""
	if data, err := os.ReadFile(path); err == nil {
		content = string(data)
	} else if !os.IsNotExist(err) {
		return err
	}

	if strings.Contains(content, line) {
		return nil
	}

	if content != "" && !strings.HasSuffix(content, "\n") {
		content += "\n"
	}
	content += line + "\n"

	return os.WriteFile(path, []byte(content), 0644)
}

func applyTerminalRuntimeHooks(ctx *target.Context, enabled map[string]bool) {
	if isTerminalEnabled(enabled, "kitty") {
		kittyConf := filepath.Join(ctx.XDGConfigHome(), "kitty", "kitty.conf")
		_ = ensureLineInFile(kittyConf, "include current-theme.conf")
		if _, err := lookPath("kitty"); err == nil {
			_ = runCommand("pkill", "--signal", "SIGUSR1", "-x", "kitty")
		}
	}

	if isTerminalEnabled(enabled, "alacritty") {
		alacrittyMain := filepath.Join(ctx.XDGConfigHome(), "alacritty", "alacritty.toml")
		_ = ensureLineInFile(alacrittyMain, `import = ["~/.config/alacritty/colors.toml"]`)
		_ = touchFile(alacrittyMain)
	}

	if isTerminalEnabled(enabled, "wezterm") {
		weztermMain := filepath.Join(ctx.XDGConfigHome(), "wezterm", "wezterm.lua")
		_ = touchFile(weztermMain)
	}

	if isTerminalEnabled(enabled, "foot") {
		if _, err := lookPath("foot"); err == nil {
			_ = runCommand("pkill", "-USR1", "foot")
		}
	}

	if isTerminalEnabled(enabled, "konsole") {
		if _, err := lookPath("qdbus6"); err == nil {
			_ = runCommand("qdbus6", "org.kde.konsole")
		}
	}
}

func touchFile(path string) error {
	if _, err := os.Stat(path); err != nil {
		if os.IsNotExist(err) {
			return nil
		}
		return err
	}
	now := time.Now()
	return os.Chtimes(path, now, now)
}

func pickPalette(palette map[string]string, key, fallback string) string {
	value := strings.TrimSpace(palette[key])
	if normalized, ok := colorutil.NormalizeHexLower(value); ok {
		return normalized
	}
	if normalized, ok := colorutil.NormalizeHexLower(fallback); ok {
		return normalized
	}
	return "#000000"
}

func pickTerminal(terminal map[string]string, key, fallback string) string {
	value := strings.TrimSpace(terminal[key])
	if normalized, ok := colorutil.NormalizeHexLower(value); ok {
		return normalized
	}
	if normalized, ok := colorutil.NormalizeHexLower(fallback); ok {
		return normalized
	}
	return "#000000"
}

func stripHash(value string) string {
	normalized, ok := colorutil.NormalizeHexLower(value)
	if !ok {
		return "000000"
	}
	return strings.TrimPrefix(normalized, "#")
}

func toRGB(value string) string {
	csv, ok := colorutil.HexToRGBCSV(value, false)
	if !ok {
		return "0,0,0"
	}
	return csv
}
