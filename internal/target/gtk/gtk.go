type Applier struct{}

func (a *Applier) Apply(ctx *target.Context) error {
	enableApps := ctx.Config.WallpaperTheming.EnableAppsAndShell
	enableQt := ctx.Config.WallpaperTheming.EnableQtApps
	if !enableApps && !enableQt {
		return nil
	}

	colors, err := ctx.ReadPaletteJSON()
	if err != nil {
		colors, err = ctx.ReadColorsJSON()
		if err != nil {
			return fmt.Errorf("read colors: %w", err)
		}
	}

	pick := func(key, fallback string) string {
		if v, ok := colors[key]; ok && v != "" {
			return v
		}
		return fallback
	}

	bg := pick("background", "#1e1e2e")
	fg := pick("on_background", "#cdd6f4")
	primary := pick("primary", "#cba6f7")
	onPrimary := pick("on_primary", "#1e1e2e")
	primaryContainer := pick("primary_container", adjustColor(primary, -26))
	onPrimaryContainer := pick("on_primary_container", fg)
	surface := pick("surface", bg)
	onSurface := pick("on_surface", fg)
	surfaceContainer := pick("surface_container", adjustColor(bg, 13))
	surfaceContainerLow := pick("surface_container_low", adjustColor(bg, 9))
	surfaceContainerHigh := pick("surface_container_high", adjustColor(bg, 23))
	surfaceContainerHighest := pick("surface_container_highest", adjustColor(bg, 34))
	surfaceDim := pick("surface_dim", adjustColor(bg, -10))
	outlineVariant := pick("outline_variant", adjustColor(bg, 52))
	errorColor := pick("error", "#ff6b6b")
	tertiary := pick("tertiary", "#ffa94d")
	secondary := pick("secondary", "#69db7c")
	_ = surfaceDim
	_ = outlineVariant
	_ = errorColor
	_ = tertiary
	_ = secondary
	_ = primaryContainer
	secondaryContainer := pick("secondary_container", adjustColor(primaryContainer, 8))
	_ = secondaryContainer

	rowHoverBg := blendHex(surfaceContainer, primary, 12)
	rowActiveBg := blendHex(surfaceContainerHigh, primary, 18)
	rowActiveHoverBg := blendHex(surfaceContainerHigh, primary, 26)

	configHome := ctx.XDGConfigHome()

	gtk4CSS := generateGTK4CSS(bg, fg, primary, onPrimary, primaryContainer, onPrimaryContainer,
		surface, onSurface, surfaceContainer, surfaceContainerLow, surfaceContainerHigh,
		surfaceContainerHighest, outlineVariant, rowHoverBg, rowActiveBg, rowActiveHoverBg)
	gtk4Path := filepath.Join(configHome, "gtk-4.0", "gtk.css")
	os.MkdirAll(filepath.Dir(gtk4Path), 0755)
	os.Remove(gtk4Path)
	os.WriteFile(gtk4Path, []byte(gtk4CSS), 0644)

	gtk3CSS := generateGTK3CSS(bg, fg, primary, onPrimary, surfaceContainer, surfaceContainerLow,
		onSurface, surfaceDim, rowHoverBg, rowActiveBg, fg, primaryContainer,
		surfaceContainerHigh, surfaceContainerHighest, outlineVariant, errorColor, tertiary, secondary)
	gtk3Path := filepath.Join(configHome, "gtk-3.0", "gtk.css")
	os.MkdirAll(filepath.Dir(gtk3Path), 0755)
	os.Remove(gtk3Path)
	os.WriteFile(gtk3Path, []byte(gtk3CSS), 0644)

	if enableQt {
		kdeglobals := generateKDEGlobals(bg, fg, primary, onPrimary, surface, surfaceDim,
			surfaceContainer, surfaceContainerHigh, surfaceContainerLow, surfaceContainerHighest,
			outlineVariant, primaryContainer, errorColor, tertiary, secondary, rowHoverBg,
			rowActiveBg, rowActiveHoverBg, fg)
		kdeglobalsPath := filepath.Join(ctx.Home(), ".config", "kdeglobals")
		os.WriteFile(kdeglobalsPath, []byte(kdeglobals), 0644)

		dataHome := ctx.XDGDataHome()
		darklyPath := filepath.Join(dataHome, "color-schemes", "Darkly.colors")
		os.MkdirAll(filepath.Dir(darklyPath), 0755)
		darklyContent := generateDarklyColors(bg, fg, primary, onPrimary, surface,
			surfaceDim, surfaceContainer, surfaceContainerHigh, surfaceContainerLow,
			surfaceContainerHighest, outlineVariant, primaryContainer, errorColor,
			tertiary, secondary, rowHoverBg, rowActiveBg, rowActiveHoverBg)
		os.WriteFile(darklyPath, []byte(darklyContent), 0644)

		writeQt6ct(darklyPath, ctx)
		writeQt5ct(darklyPath, ctx)
	}

	return nil
}

