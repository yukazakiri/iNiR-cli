func adjustColor(hex string, amount int) string {
	hex = strings.TrimPrefix(hex, "#")
	if len(hex) < 6 {
		return "#000000"
	}
	r := clampInt(hexByte(hex[0:2])+amount, 0, 255)
	g := clampInt(hexByte(hex[2:4])+amount, 0, 255)
	b := clampInt(hexByte(hex[4:6])+amount, 0, 255)
	return fmt.Sprintf("#%02x%02x%02x", r, g, b)
}

func blendHex(base, top string, pct int) string {
	bR, bG, bB := hexToRGB(base)
	tR, tG, tB := hexToRGB(top)
	inv := 100 - pct
	r := (bR*inv + tR*pct + 50) / 100
	g := (bG*inv + tG*pct + 50) / 100
	b := (bB*inv + tB*pct + 50) / 100
	return fmt.Sprintf("#%02X%02X%02X", r, g, b)
}

func hexToRGB(hex string) (int, int, int) {
	hex = strings.TrimPrefix(hex, "#")
	if len(hex) < 6 {
		return 0, 0, 0
	}
	return hexByte(hex[0:2]), hexByte(hex[2:4]), hexByte(hex[4:6])
}

func hexToRGBComma(hex string) string {
	r, g, b := hexToRGB(hex)
	return fmt.Sprintf("%d,%d,%d", r, g, b)
}

func hexByte(s string) int {
	v := 0
	for _, c := range s {
		v *= 16
		if c >= '0' && c <= '9' {
			v += int(c - '0')
		} else if c >= 'a' && c <= 'f' {
			v += int(c-'a') + 10
		} else if c >= 'A' && c <= 'F' {
			v += int(c-'A') + 10
		}
	}
	return v
}

func clampInt(v, lo, hi int) int {
	if v < lo {
		return lo
	}
	if v > hi {
		return hi
	}
	return v
}
