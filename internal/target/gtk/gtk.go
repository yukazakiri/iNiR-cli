func writeQt6ct(darklyPath string, ctx *target.Context) {
	qt6ctPath := filepath.Join(ctx.XDGConfigHome(), "qt6ct", "qt6ct.conf")
	os.MkdirAll(filepath.Dir(qt6ctPath), 0755)
	content := fmt.Sprintf("[Appearance]\ncolor_scheme_path=%s\ncustom_palette=true\nicon_theme=Adwaita\nstyle=Darkly\n", darklyPath)
	os.WriteFile(qt6ctPath, []byte(content), 0644)
}

